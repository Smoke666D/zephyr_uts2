#define INA228_THREAD_PRIORITY    4
/**
 *  @brief Перечисление физических измерительных каналов (датчиков)
 */
typedef enum cur_sens_channel
{
    CUR_SENS_BRD_LOW     = 0,
    CUR_SENS_BRD_HIGH    = 1,
    CUR_SENS_VDUT2       = 2,
    CUR_SENS_VDUT3       = 3,
    CUR_SENS_VIN         = 4,
    CUR_SENS_DCDC_3_3    = 5,
    CUR_SENS_VDOUT_PWR   = 6,
    CUR_SENS_VD5         = 7,
    CUR_SENS_NUM_SENSORS = 8,
} cur_sens_channel_t;

/**
 *  @brief Структура одиночного измерения датчика
 */
typedef struct sensor_reading 
{
    double voltage;
    double current;
} sensor_reading_t;

/**
 *  @brief Пакетное сообщение ZBUS с результатами опроса всех датчиков
 */
typedef struct ina_batch_msg 
{
    sensor_reading_t sensors[CUR_SENS_NUM_SENSORS];
} ina_batch_msg_t;

/**
 *  @brief Пакетное сообщение ZBUS с накопленной энергией всех датчиков
 */
typedef struct ina_energy_msg 
{
    double energy[CUR_SENS_NUM_SENSORS];
} ina_energy_msg_t;

